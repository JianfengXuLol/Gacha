							<br>
							<p>Sumeru City is known across Teyvat as the "City of Wisdom."[1] It is under the jurisdiction of the Sumeru Akademiya and is built on top of the Divine Tree. The city features many districts and points of interest, such as the Grand Bazaar, Zubayr Theater, Treasures Street, and more. Located in the middle to top half of the tree is The Akademiya, which serves as the main base of the Sumeru Akademiya. On top of The Akademiya rests the Sanctuary of Surasthana, which is a treetop palace where Lesser Lord Kusanali resides.</p>
